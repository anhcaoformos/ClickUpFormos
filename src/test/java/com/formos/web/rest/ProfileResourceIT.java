    @Test
    @Transactional
    void createProfile() throws Exception {
        int databaseSizeBeforeCreate = profileRepository.findAll().size();
        // Create the Profile
        restProfileMockMvc
            .perform(post(ENTITY_API_URL).contentType(MediaType.APPLICATION_JSON).content(TestUtil.convertObjectToJsonBytes(profile)))
            .andExpect(status().isCreated());

        // Validate the Profile in the database
        List<Profile> profileList = profileRepository.findAll();
        assertThat(profileList).hasSize(databaseSizeBeforeCreate + 1);
        Profile testProfile = profileList.get(profileList.size() - 1);
        assertThat(testProfile.getName()).isEqualTo(DEFAULT_NAME);
        assertThat(testProfile.getUsername()).isEqualTo(DEFAULT_USERNAME);
        assertThat(testProfile.getPassword()).isEqualTo(DEFAULT_PASSWORD);
        assertThat(testProfile.getApiKey()).isEqualTo(DEFAULT_API_KEY);
        assertThat(testProfile.getToken()).isEqualTo(DEFAULT_TOKEN);
        assertThat(testProfile.getBaseUrl()).isEqualTo(DEFAULT_BASE_URL);
    }

