    @Test
    @Transactional
    void putExistingProfile() throws Exception {
        // Initialize the database
        profileRepository.saveAndFlush(profile);

        int databaseSizeBeforeUpdate = profileRepository.findAll().size();

        // Update the profile
        Profile updatedProfile = profileRepository.findById(profile.getId()).orElseThrow();
        // Disconnect from session so that the updates on updatedProfile are not directly saved in db
        em.detach(updatedProfile);
        updatedProfile
            .name(UPDATED_NAME)
            .username(UPDATED_USERNAME)
            .password(UPDATED_PASSWORD)
            .apiKey(UPDATED_API_KEY)
            .token(UPDATED_TOKEN)
            .baseUrl(UPDATED_BASE_URL);

        restProfileMockMvc
            .perform(
                put(ENTITY_API_URL_ID, updatedProfile.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(updatedProfile))
            )
            .andExpect(status().isOk());

        // Validate the Profile in the database
        List<Profile> profileList = profileRepository.findAll();
        assertThat(profileList).hasSize(databaseSizeBeforeUpdate);
        Profile testProfile = profileList.get(profileList.size() - 1);
        assertThat(testProfile.getName()).isEqualTo(UPDATED_NAME);
        assertThat(testProfile.getUsername()).isEqualTo(UPDATED_USERNAME);
        assertThat(testProfile.getPassword()).isEqualTo(UPDATED_PASSWORD);
        assertThat(testProfile.getApiKey()).isEqualTo(UPDATED_API_KEY);
        assertThat(testProfile.getToken()).isEqualTo(UPDATED_TOKEN);
        assertThat(testProfile.getBaseUrl()).isEqualTo(UPDATED_BASE_URL);
    }

